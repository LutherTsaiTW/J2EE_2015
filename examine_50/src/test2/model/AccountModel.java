package test2.model;

public class AccountModel {
	private int id;
	private String name;
	private int count;
	
	public int getId() {
		return id;
	}
	
	public void setId (int InputId) {
		this.id = InputId;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName (String InputName) {
		this.name = InputName;
	}
	
	public int getCount() {
		return count;
	}
	
	public void setCount (int InputCount) {
		this.count = InputCount;
	}
}
