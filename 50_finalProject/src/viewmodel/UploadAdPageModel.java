package viewmodel;

public class UploadAdPageModel {
	private String adOwnerToken;
	
	private String adTitle;
	
	private String adDes;
	
	private String adImageLink;
	
	private String adRef;
	
	private String adStartDate;
	
	private String adEndDate;
	
	private String adImageHeight;
	
	private String adImageWidth;
	
	private String feeCount;

	public String getAdOwnerToken() {
		return adOwnerToken;
	}

	public void setAdOwnerToken(String adOwnerToken) {
		this.adOwnerToken = adOwnerToken;
	}

	public String getAdTitle() {
		return adTitle;
	}

	public void setAdTitle(String adTitle) {
		this.adTitle = adTitle;
	}

	public String getAdDes() {
		return adDes;
	}

	public void setAdDes(String adDes) {
		this.adDes = adDes;
	}

	public String getAdImageLink() {
		return adImageLink;
	}

	public void setAdImageLink(String adImageLink) {
		this.adImageLink = adImageLink;
	}

	public String getAdRef() {
		return adRef;
	}

	public void setAdRef(String adRef) {
		this.adRef = adRef;
	}

	public String getAdStartDate() {
		return adStartDate;
	}

	public void setAdStartDate(String adStartDate) {
		this.adStartDate = adStartDate;
	}

	public String getAdEndDate() {
		return adEndDate;
	}

	public void setAdEndDate(String adEndDate) {
		this.adEndDate = adEndDate;
	}

	public String getAdImageHeight() {
		return adImageHeight;
	}

	public void setAdImageHeight(String adImageHeight) {
		this.adImageHeight = adImageHeight;
	}

	public String getAdImageWidth() {
		return adImageWidth;
	}

	public void setAdImageWidth(String adImageWidth) {
		this.adImageWidth = adImageWidth;
	}

	public String getFeeCount() {
		return feeCount;
	}

	public void setFeeCount(String feeCount) {
		this.feeCount = feeCount;
	}
	
	
}
